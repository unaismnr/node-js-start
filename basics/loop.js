//for loop
for (i = 0; i <= 10; i = i + 2) {
    console.log(i)
}

//star patter
var n = 5;
for (i = 1; i <= n; i++) {
    var star = '* ';
    console.log(star.repeat(i))
}

//while loop
var i = 0;
while (i <= 10) {
    console.log(i);
    i = i + 2;
}

//do while loop
var i = 0;
do {
    console.log(i);
    i = i + 2;
} while (i <= 10);
